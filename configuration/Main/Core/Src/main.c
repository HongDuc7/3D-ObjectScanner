uint32_t Read_ADC(void) {
    HAL_ADC_Start(&hadc1);                     // Bắt đầu chuyển đổi
    HAL_ADC_PollForConversion(&hadc1, 10);     // Chờ ADC
    uint32_t value = HAL_ADC_GetValue(&hadc1); // Lấy giá trị ADC
    HAL_ADC_Stop(&hadc1);
    return value;
}

